package pl.wr.rss.rssfiz.show.model;

public class MixedNumber {

    Long total;
    Long numerator;
    Long denumerator;
    Integer decimalPower;

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }

    public Long getNumerator() {
        return numerator;
    }

    public void setNumerator(Long numerator) {
        this.numerator = numerator;
    }

    public Long getDenumerator() {
        return denumerator;
    }

    public void setDenumerator(Long denumerator) {
        this.denumerator = denumerator;
    }

    public Integer getDecimalPower() {
        return decimalPower;
    }

    public void setDecimalPower(Integer decimalPower) {
        this.decimalPower = decimalPower;
    }
}
